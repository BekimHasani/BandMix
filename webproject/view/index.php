    <script type="text/javascript">

        var slides = document.querySelector('.con');
        var slidesContent = document.querySelectorAll('.slideContent');

        var prevBtn = document.querySelector('#prevBtn');
        var nextBtn = document.querySelector('#nextBtn');

        let counter = 1;
        const size =  slidesContent[0].clientWidth;

        slides.style.transform = 'translateX('+(-size * counter) +'px)';

        nextBtn.addEventListener('click',()=>{
          if(counter >= slides.length - 1) return;
          slides.style.transition = "transform 0.0001s ease-in-out";
          counter++;
          slides.style.transform = 'translateX('+(-size * counter) +'px)';
        });

        prevBtn.addEventListener('click',()=>{
          if(counter <= 0) return;
          slides.style.transition = "transform 0.0001s ease-in-out";
          counter--;
          slides.style.transform = 'translateX('+(-size * counter) +'px)';
        });

        slides.addEventListener('transitionend', ()=>{
          if(slidesContent[counter].id === 'lastClone'){
              slides.style.transition = "none";
              counter = slidesContent.length - 2;
              slides.style.transform = 'translateX('+(-size * counter) +'px)';
          }
          if(slidesContent[counter].id === 'firstClone'){
              slides.style.transition = "none";
              counter = slidesContent.length - counter;
              slides.style.transform = 'translateX('+(-size * counter) +'px)';
          }
        });

   </script>
